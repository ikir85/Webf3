//  ### ~ l'Addition 

var nb1, nb2, resultat;
nb1= 10;
nb2=5;


// L'addition de nb1 et nb2 avec l'operateur " +"
resultat = nb1 + nb2;
console.log( 'Addition de nb1 et nb2 = ' + resultat );

// La soustraction de nb1 et nb2 avec l'operateur " -"
resultat = nb1 - nb2;
console.log( 'Addition de nb1 et nb2 = ' + resultat );


// La multiplication de nb1 et nb2 avec l'operateur " * "
resultat = nb1 * nb2;
console.log( 'Addition de nb1 et nb2 = ' + resultat );

// La division de nb1 et nb2 avec l'operateur " * "
resultat = nb1 / nb2;
console.log( 'Addition de nb1 et nb2 = ' + resultat );

// Le modulo de nb1 et nb2 avec l'operateur " * "
// Le modulo correspond au reste de la division 
nb1 = 11;
resultat = nb1 % nb2;
console.log( 'Addition de nb1 et nb2 = ' + resultat );

